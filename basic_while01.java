public class basic_while01 {
    public static void main(String[] args) {
        System.out.println("basic while loop program");
        System.out.println("----------------------------");
        int round = 1;
        while (round <= 12) {
            System.out.println("7 x " + round + " = " + (7 * round));
            round += 1;
        }
        System.out.println("----------------------------");
    }
}
